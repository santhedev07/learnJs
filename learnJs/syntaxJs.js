// Example of variable declaration and assignment in JavaScript
// Using 'let' to declare variables
let a, b, c;
a = 5;
b = 10;
c = a + b;
console.log(c); // Output: 15

// Example of a variable declaration and assignment in JavaScript
// using var keyword
var x;
let y;
x = 20;
y = 30;

let z = x + y;
console.log(z); // Output: 50

// Example of a variable declaration and assignment in JavaScript
// using const keyword
const pi = 3.14;
var radius = 5;
let area = pi * radius * radius;
console.log(area); // Output: 78.5

// opetator precedence example
let m = 10;
let n = 5;
let result = (m + n) * 2; // Multiplication has higher precedence than addition
console.log(result); // Output: 20
